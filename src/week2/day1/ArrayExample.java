package week2.day1;

public class ArrayExample {
  public static void main(String[] args) {

    int[] mas = new int[3];

    mas[1] = 15;

    System.out.println(mas[0]);
    System.out.println(mas[1]);
    System.out.println(mas[2]);

    boolean[] boolMas = {true, false, false, true};

    System.out.println(boolMas[0]);
    System.out.println(boolMas[1]);
    System.out.println(boolMas[2]);
    System.out.println(boolMas[3]);


  }
}
